// ---------------------------------------------------------------------------
// Helper utilities
// ---------------------------------------------------------------------------

function nodeToRef(node) {
  return sanitizeApiName(`${node.node_type}_${node.id.replace(/-/g, '').slice(0, 12)}`);
}

function nodeToRefById(nodeId, allNodes) {
  const node = allNodes.find((n) => n.id === nodeId);
  if (!node || node.node_type === 'exit') return null;
  return nodeToRef(node);
}

function getNextNodeRef(nodeId, outgoing, allNodes) {
  const edges = outgoing[nodeId];
  if (!edges || edges.length === 0) return null;
  return nodeToRefById(edges[0].target_node_id, allNodes);
}

function buildOutgoingMap(edges) {
  const map = {};
  for (const edge of edges) {
    if (!map[edge.source_node_id]) map[edge.source_node_id] = [];
    map[edge.source_node_id].push(edge);
  }
  return map;
}

/**
 * Simple topological sort using Kahn's algorithm.
 * Ensures nodes are processed in dependency order.
 */
function topologicalSort(nodes, edges) {
  const inDegree = {};
  const adjacency = {};

  for (const node of nodes) {
    inDegree[node.id] = 0;
    adjacency[node.id] = [];
  }

  for (const edge of edges) {
    adjacency[edge.source_node_id]?.push(edge.target_node_id);
    if (inDegree[edge.target_node_id] !== undefined) {
      inDegree[edge.target_node_id]++;
    }
  }

  const queue = nodes.filter((n) => inDegree[n.id] === 0);
  const sorted = [];

  while (queue.length > 0) {
    const node = queue.shift();
    sorted.push(node);

    for (const neighborId of adjacency[node.id] || []) {
      inDegree[neighborId]--;
      if (inDegree[neighborId] === 0) {
        const neighborNode = nodes.find((n) => n.id === neighborId);
        if (neighborNode) queue.push(neighborNode);
      }
    }
  }

  // Add any remaining nodes (for disconnected graphs)
  for (const node of nodes) {
    if (!sorted.includes(node)) sorted.push(node);
  }

  return sorted;
}

function escapeXml(str) {
  if (!str) return '';
  return String(str)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&apos;');
}

