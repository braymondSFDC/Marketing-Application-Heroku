'use strict';

const { sanitizeApiName } = require('./templateService');

const API_VERSION = '62.0';

/**
 * Transform a React Flow canvas state (nodes + edges) into Salesforce Flow metadata XML.
 * This is the core translation engine of the Journey Builder.
 *
 * Supported node types:
 *   trigger (Segment/Schedule/APIEvent) → <start>
 *   email   → <actionCalls> emailSimple
 *   wait    → <waits> AlarmEvent
 *   split   → <decisions> random percentage
 *   condition → <decisions> field criteria
 *   update  → <recordUpdates>
 *   webhook → <actionCalls> apex (JourneyWebhookCallout)
 *   exit    → (no element — path terminates)
 */
function generateFlowXml(journey, nodes, edges, templateMap) {
  const flowApiName = sanitizeApiName(`JB_${journey.name.replace(/\s+/g, '_')}_${journey.id.slice(0, 8)}`);
  const sortedNodes = topologicalSort(nodes, edges);

  // Build connectivity map
  const outgoing = buildOutgoingMap(edges);
  const elements = [];
  const variables = [];
  const textTemplates = [];

  // Start element
  const triggerNode = sortedNodes.find((n) => n.node_type === 'trigger');
  const startTarget = getNextNodeRef(triggerNode?.id, outgoing, sortedNodes);

  let startXml = buildStartElement(triggerNode, startTarget);

  // Process each non-trigger node
  for (const node of sortedNodes) {
    if (node.node_type === 'trigger') continue;
    if (node.node_type === 'exit') continue; // exits are just path terminators

    const nodeRef = nodeToRef(node);
    const nextRef = getNextNodeRef(node.id, outgoing, sortedNodes);

    switch (node.node_type) {
      case 'email':
        elements.push(buildEmailElement(node, nodeRef, nextRef, templateMap, textTemplates));
        break;
      case 'wait':
        elements.push(buildWaitElement(node, nodeRef, nextRef));
        break;
      case 'condition':
        elements.push(buildConditionElement(node, nodeRef, outgoing, sortedNodes, variables));
        break;
      case 'split':
        elements.push(buildSplitElement(node, nodeRef, outgoing, sortedNodes));
        break;
      case 'update':
        elements.push(buildUpdateElement(node, nodeRef, nextRef));
        break;
      case 'webhook':
        elements.push(buildWebhookElement(node, nodeRef, nextRef));
        break;
      default:
        console.warn(`[FlowGenerator] Unknown node type: ${node.node_type}`);
    }
  }

  // Assemble full XML
  const xml = `<?xml version="1.0" encoding="UTF-8"?>
<Flow xmlns="http://soap.sforce.com/2006/04/metadata">
    <apiVersion>${API_VERSION}</apiVersion>
    <processType>AutoLaunchedFlow</processType>
    <status>Draft</status>
    <label>${escapeXml(journey.name)}</label>
    <description>Auto-generated by Heroku Journey Builder. Journey ID: ${journey.id}</description>
${startXml}
${elements.join('\n')}
${textTemplates.join('\n')}
${variables.join('\n')}
</Flow>`;

  return { flowApiName, xml };
}

/**
 * Generate the package.xml for the deployment ZIP.
 */
function generatePackageXml(flowApiName) {
  return `<?xml version="1.0" encoding="UTF-8"?>
<Package xmlns="http://soap.sforce.com/2006/04/metadata">
    <types>
        <members>${flowApiName}</members>
        <name>Flow</name>
    </types>
    <version>${API_VERSION}</version>
</Package>`;
}

