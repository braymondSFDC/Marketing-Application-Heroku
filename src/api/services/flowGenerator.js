function buildStartElement(triggerNode, targetRef) {
  const config = triggerNode?.config || {};
  const triggerType = config.triggerType || 'PlatformEvent';
  const triggerObject = config.triggerObject || 'Journey_Entry__e';

  return `
    <start>
        <locationX>50</locationX>
        <locationY>0</locationY>
        <connector>
            <targetReference>${targetRef}</targetReference>
        </connector>
        <object>${triggerObject}</object>
        <triggerType>${triggerType}</triggerType>
    </start>`;
}

function buildEmailElement(node, ref, nextRef, templateMap, textTemplates) {
  const config = node.config || {};
  const templateInfo = templateMap?.[node.id];
  const bodyRef = `${ref}_Body`;

  // Add text template for email body
  textTemplates.push(`
    <textTemplates>
        <name>${bodyRef}</name>
        <isViewedAsPlainText>false</isViewedAsPlainText>
        <text>${escapeXml(config.htmlBody || '<p>Email content</p>')}</text>
    </textTemplates>`);

  const connector = nextRef
    ? `<connector><targetReference>${nextRef}</targetReference></connector>`
    : '';

  return `
    <actionCalls>
        <name>${ref}</name>
        <label>${escapeXml(config.name || 'Send Email')}</label>
        <locationX>${node.position_x || 176}</locationX>
        <locationY>${node.position_y || 134}</locationY>
        <actionName>emailSimple</actionName>
        <actionType>emailSimple</actionType>
        ${connector}
        <inputParameters>
            <name>emailAddresses</name>
            <value>
                <elementReference>$Record.Email__c</elementReference>
            </value>
        </inputParameters>
        <inputParameters>
            <name>emailSubject</name>
            <value>
                <stringValue>${escapeXml(config.subject || 'Journey Email')}</stringValue>
            </value>
        </inputParameters>
        <inputParameters>
            <name>emailBody</name>
            <value>
                <elementReference>${bodyRef}</elementReference>
            </value>
        </inputParameters>
    </actionCalls>`;
}

function buildWaitElement(node, ref, nextRef) {
  const config = node.config || {};
  const duration = config.duration || 1;
  const unit = config.unit || 'Days';

  const connector = nextRef
    ? `<defaultConnector><targetReference>${nextRef}</targetReference></defaultConnector>`
    : '';

  return `
    <waits>
        <name>${ref}</name>
        <label>${escapeXml(config.name || `Wait ${duration} ${unit}`)}</label>
        <locationX>${node.position_x || 176}</locationX>
        <locationY>${node.position_y || 300}</locationY>
        ${connector}
        <waitEvents>
            <name>${ref}_Event</name>
            <conditionLogic>and</conditionLogic>
            <connector>
                <targetReference>${nextRef || ref}</targetReference>
            </connector>
            <eventType>AlarmEvent</eventType>
            <inputParameters>
                <name>AlarmTime</name>
                <value>
                    <elementReference>$Flow.CurrentDateTime</elementReference>
                </value>
            </inputParameters>
            <inputParameters>
                <name>TimeOffset</name>
                <value>
                    <numberValue>${duration}</numberValue>
                </value>
            </inputParameters>
            <inputParameters>
                <name>TimeOffsetUnit</name>
                <value>
                    <stringValue>${unit}</stringValue>
                </value>
            </inputParameters>
        </waitEvents>
    </waits>`;
}

function buildConditionElement(node, ref, outgoing, allNodes, variables) {
  const config = node.config || {};
  const edges = outgoing[node.id] || [];

  // Find Yes/No paths
  const yesEdge = edges.find((e) => e.label === 'Yes' || e.label === 'true');
  const noEdge = edges.find((e) => e.label === 'No' || e.label === 'false' || !e.label || e !== yesEdge);

  const yesTarget = yesEdge ? nodeToRefById(yesEdge.target_node_id, allNodes) : null;
  const noTarget = noEdge ? nodeToRefById(noEdge.target_node_id, allNodes) : null;

  const field = config.field || 'EmailOpenedFlag';
  const operator = config.operator || 'EqualTo';
  const value = config.value ?? 'true';

  const defaultConnector = noTarget
    ? `<defaultConnector><targetReference>${noTarget}</targetReference></defaultConnector>`
    : '';

  const ruleConnector = yesTarget
    ? `<connector><targetReference>${yesTarget}</targetReference></connector>`
    : '';

  return `
    <decisions>
        <name>${ref}</name>
        <label>${escapeXml(config.name || 'Condition Check')}</label>
        <locationX>${node.position_x || 176}</locationX>
        <locationY>${node.position_y || 450}</locationY>
        ${defaultConnector}
        <defaultConnectorLabel>No</defaultConnectorLabel>
        <rules>
            <name>${ref}_Yes</name>
            <conditionLogic>and</conditionLogic>
            <conditions>
                <leftValueReference>${field}</leftValueReference>
                <operator>${operator}</operator>
                <rightValue>
                    <stringValue>${escapeXml(String(value))}</stringValue>
                </rightValue>
            </conditions>
            ${ruleConnector}
            <label>Yes</label>
        </rules>
    </decisions>`;
}

function buildSplitElement(node, ref, outgoing, allNodes) {
  const config = node.config || {};
  const edges = outgoing[node.id] || [];
  const paths = config.paths || [{ label: 'Path A', percentage: 50 }, { label: 'Path B', percentage: 50 }];

  // Map edges to paths
  const rules = paths.map((p, i) => {
    const edge = edges[i];
    const target = edge ? nodeToRefById(edge.target_node_id, allNodes) : null;
    const connector = target
      ? `<connector><targetReference>${target}</targetReference></connector>`
      : '';

    return `
        <rules>
            <name>${ref}_${sanitizeApiName(p.label)}</name>
            <conditionLogic>and</conditionLogic>
            <conditions>
                <leftValueReference>SplitRandom</leftValueReference>
                <operator>LessThanOrEqualTo</operator>
                <rightValue>
                    <numberValue>${p.percentage}</numberValue>
                </rightValue>
            </conditions>
            ${connector}
            <label>${escapeXml(p.label)} (${p.percentage}%)</label>
        </rules>`;
  });

  // Default path is last rule's target
  const lastEdge = edges[edges.length - 1];
  const defaultTarget = lastEdge ? nodeToRefById(lastEdge.target_node_id, allNodes) : null;
  const defaultConnector = defaultTarget
    ? `<defaultConnector><targetReference>${defaultTarget}</targetReference></defaultConnector>`
    : '';

  return `
    <decisions>
        <name>${ref}</name>
        <label>${escapeXml(config.name || 'A/B Split')}</label>
        <locationX>${node.position_x || 176}</locationX>
        <locationY>${node.position_y || 400}</locationY>
        ${defaultConnector}
        <defaultConnectorLabel>Default</defaultConnectorLabel>
${rules.join('\n')}
    </decisions>`;
}

